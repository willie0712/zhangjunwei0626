const langButton = document.querySelector(".lang-toggle");
const menuToggle = document.querySelector(".menu-toggle");
const navPanel = document.querySelector(".nav-panel");

function applyLanguage(lang) {
  const dictionary = translations[lang] || translations.zh;
  document.documentElement.lang = lang === "zh" ? "zh-Hant" : "en";

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  langButton.textContent = lang === "zh" ? "中文 / EN" : "EN / 中文";
  localStorage.setItem("willie-site-lang", lang);
}

langButton.addEventListener("click", () => {
  const current = localStorage.getItem("willie-site-lang") || "zh";
  applyLanguage(current === "zh" ? "en" : "zh");
});

menuToggle.addEventListener("click", () => {
  const isOpen = navPanel.classList.toggle("is-open");
  menuToggle.setAttribute("aria-expanded", String(isOpen));
});

document.querySelectorAll(".nav-panel a").forEach((link) => {
  link.addEventListener("click", () => {
    navPanel.classList.remove("is-open");
    menuToggle.setAttribute("aria-expanded", "false");
  });
});

applyLanguage(localStorage.getItem("willie-site-lang") || "zh");

// ===== 背景音樂開關（預設開啟） =====
const musicToggle = document.getElementById('musicToggle');
const musicContainer = document.getElementById('musicContainer');
let musicPlaying = true;

if (musicToggle && musicContainer) {
  musicToggle.addEventListener('click', function() {
    musicPlaying = !musicPlaying;
    
    if (musicPlaying) {
      musicContainer.style.display = 'block';
      musicToggle.classList.add('playing');
      musicToggle.innerHTML = '<span class="music-icon">🔊</span><span class="music-text">關閉音樂</span>';
    } else {
      musicContainer.style.display = 'none';
      musicToggle.classList.remove('playing');
      musicToggle.innerHTML = '<span class="music-icon">🔇</span><span class="music-text">播放音樂</span>';
    }
  });
}
